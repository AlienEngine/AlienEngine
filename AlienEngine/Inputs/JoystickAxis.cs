namespace AlienEngine
{
    /// <summary>
    /// Joystick axis.
    /// </summary>
    public enum JoystickAxis
    {
        /// <summary>
        /// The X axis.
        /// </summary>
        AxisX,

        /// <summary>
        /// The Y axis.
        /// </summary>
        AxisY,

        /// <summary>
        /// The Z axis.
        /// </summary>
        AxisZ,

        /// <summary>
        /// The rotation axis over the X coordinate.
        /// </summary>
        RotationAxisZ,

        /// <summary>
        /// The rotation axis over the Y coordinate.
        /// </summary>
        RotationAxisY,

        /// <summary>
        /// The rotation axis over the Z coordinate.
        /// </summary>
        RotationAxisX
    }
}
